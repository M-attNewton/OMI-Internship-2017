% A summary of all the results for varying sampling frequency, with each dependency method in a seperate figure.

function [str2] = summarise_results_resampling(largest_R_Sfiltert,...
    largest_R_Sresamplet,largest_R_Cresamplet,largest_R_Cfiltert,...
    merit_R_Sfiltert,merit_R_Sresamplet,merit_R_Cresamplet,merit_R_Cfiltert,...
    R_Sfiltert,R_Sresamplet,R_Cresamplet,R_Cfiltert,...
    summarise_data,summarise_standard_resample,summarise_standard_filter,...
    summarise_copula_resample,summarise_copula_filter,...
    relate_feature_list,fs_range,num_freq)

% Vector containing the various frequencies.
fs = linspace(fs_range(1),fs_range(2),num_freq);

% Dummy variable to make code work.
str2 = 'done';

% Conditional statements to plot the required figures.
if summarise_data == 1
       
   if summarise_standard_resample == 1
       
       % Creates the releveant figure.
       f_standard_resampleS = figure('Name','Standard Correlation with Varying Resampling Results Summary','NumberTitle','off');
       figure(f_standard_resampleS);
       
       % Goes through each matrix (frequency) and plots the data on the
       % figure at different locations.  
       % The sizes and locations of each component will be automatically
       % assigned to fit the figure. 
       for i = 1:size(R_Sresamplet,3)
           
           % Displays the dependency matrix in a table, takes up 60% of 
           % the figure.
           table_standard_resampleS(i) = uitable(f_standard_resampleS,'Data',R_Sresamplet(:,:,i),...
               'Units','Normalized','Position',[0 (1 - (i)/size(R_Sresamplet,3)) 0.6 1/size(R_Sresamplet,3)],...
               'RowName',relate_feature_list,'ColumnName',relate_feature_list,...
               'ColumnWidth','auto');
           
           % Displays the time windows for each matrix, takes up 10% of
           % the figure.
           table_standard_resampleS2(i) = uitable(f_standard_resampleS,'Data',[],...
               'Units','Normalized','Position',[0.6 (1 - (i)/size(R_Sresamplet,3)) 0.1 1/size(R_Sresamplet,3)],...
               'RowName',{'Frequency';fs(i)},...
               'ColumnWidth','auto');

           % Displays a list of the most significant values along with
           % their relevant features, takes up 20% of the figure.
           table_standard_resampleS3(i) = uitable(f_standard_resampleS,'Data',largest_R_Sresamplet(:,:,i),...
               'Units','Normalized','Position',[0.7 (1 - (i)/size(R_Sresamplet,3)) 0.2 1/size(R_Sresamplet,3)],...
               'ColumnName',{'Feature 1';'Feature 2';'Value'},...
               'ColumnWidth','auto');
           % Displays the value of differential entropy, takes up 10% of
           % the figure.           
           table_standard_resampleS4(i) = uitable(f_standard_resampleS,'Data',real(merit_R_Sresamplet(i)),...
               'Units','Normalized','Position',[0.9 (1 - (i)/size(R_Sresamplet,3)) 0.1 1/size(R_Sresamplet,3)],...
               'ColumnName',{'Entropy'},...
               'ColumnWidth','auto');
                 
       end
              
   end
   
   if summarise_standard_filter == 1
       
       % Comments are not repeated as they are the same.
       f_standard_filterS = figure('Name','Standard Correlation Resampled and Filtered Results Summary','NumberTitle','off');
       figure(f_standard_filterS);       
      
       for i = 1:size(R_Sfiltert,3)
           
           table_standard_filterS(i) = uitable(f_standard_filterS,'Data',R_Sfiltert(:,:,i),...
               'Units','Normalized','Position',[0 (1 - (i)/size(R_Sfiltert,3)) 0.6 1/size(R_Sfiltert,3)],...
               'RowName',relate_feature_list,'ColumnName',relate_feature_list,...
               'ColumnWidth','auto');
           
           table_standard_filterS2(i) = uitable(f_standard_filterS,'Data',[],...
               'Units','Normalized','Position',[0.6 (1 - (i)/size(R_Sfiltert,3)) 0.1 1/size(R_Sfiltert,3)],...
               'RowName',{'Frequency';fs(i)},...
               'ColumnWidth','auto');

           table_standard_filterS3(i) = uitable(f_standard_filterS,'Data',largest_R_Sfiltert(:,:,i),...
               'Units','Normalized','Position',[0.7 (1 - (i)/size(R_Sfiltert,3)) 0.2 1/size(R_Sfiltert,3)],...
               'ColumnName',{'Feature 1';'Feature 2';'Value'},...
               'ColumnWidth','auto');
           
           table_standard_filterS4(i) = uitable(f_standard_filterS,'Data',real(merit_R_Sfiltert(i)),...
               'Units','Normalized','Position',[0.9 (1 - (i)/size(R_Sfiltert,3)) 0.1 1/size(R_Sfiltert,3)],...
               'ColumnName',{'Entropy'},...
               'ColumnWidth','auto');
   
       end

   end
   
   if summarise_copula_resample == 1
       
       f_copula_resampleS = figure('Name','Copula Estimator Resampled with Varying Frequency Results Summary','NumberTitle','off');
       figure(f_copula_resampleS);       
       
       for i = 1:size(R_Cresamplet,3)
           
           table_copula_resampleS(i) = uitable(f_copula_resampleS,'Data',R_Cresamplet(:,:,i),...
               'Units','Normalized','Position',[0 (1 - (i)/size(R_Cresamplet,3)) 0.6 1/size(R_Cresamplet,3)],...
               'RowName',relate_feature_list,'ColumnName',relate_feature_list,...
               'ColumnWidth','auto');
           
           table_copula_resampleS2(i) = uitable(f_copula_resampleS,'Data',[],...
               'Units','Normalized','Position',[0.6 (1 - (i)/size(R_Cresamplet,3)) 0.1 1/size(R_Cresamplet,3)],...
               'RowName',{'Frequency';fs(i)},...
               'ColumnWidth','auto');

           table_copula_resampleS3(i) = uitable(f_copula_resampleS,'Data',largest_R_Cresamplet(:,:,i),...
               'Units','Normalized','Position',[0.7 (1 - (i)/size(R_Cresamplet,3)) 0.2 1/size(R_Cresamplet,3)],...
               'ColumnName',{'Feature 1';'Feature 2';'Value'},...
               'ColumnWidth','auto');

           table_copula_resampleS4(i) = uitable(f_copula_resampleS,'Data',real(merit_R_Cresamplet(i)),...
               'Units','Normalized','Position',[0.9 (1 - (i)/size(R_Cresamplet,3)) 0.1 1/size(R_Cresamplet,3)],...
               'ColumnName',{'Entropy'},...
               'ColumnWidth','auto');
           
       end
              
   end
   
   if summarise_copula_filter == 1
       
       f_copula_filterS = figure('Name','Copula Estimator Resampled and Filtered with Varying Frequency Results Summary','NumberTitle','off');
       figure(f_copula_filterS);       
       
       for i = 1:size(R_Cfiltert,3)
           
           table_copula_filterS(i) = uitable(f_copula_filterS,'Data',R_Cfiltert(:,:,i),...
               'Units','Normalized','Position',[0 (1 - (i)/size(R_Cfiltert,3)) 0.6 1/size(R_Cfiltert,3)],...
               'RowName',relate_feature_list,'ColumnName',relate_feature_list,...
               'ColumnWidth','auto');
           
           table_copula_filterS2(i) = uitable(f_copula_filterS,'Data',[],...
               'Units','Normalized','Position',[0.6 (1 - (i)/size(R_Cfiltert,3)) 0.1 1/size(R_Cfiltert,3)],...
               'RowName',{'Frequency';fs(i)},...
               'ColumnWidth','auto');

           table_copula_filterS3(i) = uitable(f_copula_filterS,'Data',largest_R_Cfiltert(:,:,i),...
               'Units','Normalized','Position',[0.7 (1 - (i)/size(R_Cfiltert,3)) 0.2 1/size(R_Cfiltert,3)],...
               'ColumnName',{'Feature 1';'Feature 2';'Value'},...
               'ColumnWidth','auto');

           table_copula_filterS4(i) = uitable(f_copula_filterS,'Data',real(merit_R_Cfiltert(i)),...
               'Units','Normalized','Position',[0.9 (1 - (i)/size(R_Cfiltert,3)) 0.1 1/size(R_Cfiltert,3)],...
               'ColumnName',{'Entropy'},...
               'ColumnWidth','auto');
           
       end
              
   end
   
end

end

