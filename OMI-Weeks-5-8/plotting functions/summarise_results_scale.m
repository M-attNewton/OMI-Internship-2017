       f_copulaW = figure('Name','Copula Estimator Varying with Scale Results Summary','NumberTitle','off');
       figure(f_copulaW);       
       
       for i = 1:size(R_Ct,3)
           
           table_copulaW(i) = uitable(f_copulaW,'Data',R_Ct(:,:,i),...
               'Units','Normalized','Position',[0 (1 - (i)/size(R_Ct,3)) 0.6 1/size(R_Ct,3)],...
               'RowName',relate_feature_list,'ColumnName',relate_feature_list,...
