               'Units','Normalized','Position',[0.6 (1 - (i)/size(R_Ct,3)) 0.1 1/size(R_Ct,3)],...
               'RowName',{'Start Time';datestr(interval_list(i));'';'End Time';datestr(interval_list(i+1))},...
